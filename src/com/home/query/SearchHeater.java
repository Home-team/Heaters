package com.home.query;

public class SearchHeater {
    public SearchHeater() {

    }

    private String id;

    private String minPrice;
    private String maxPrice;

    private String minPower;
    private String maxPower;

    private String minCovering;
    private String maxCovering;

    private String offset;
    private String amount;

    private String type;
    private String producer;
    private String protection;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMinPrice() {
        return minPrice;
    }

    public void setMinPrice(String minPrice) {
        this.minPrice = minPrice;
    }

    public String getMaxPrice() {
        return maxPrice;
    }

    public void setMaxPrice(String maxPrice) {
        this.maxPrice = maxPrice;
    }

    public String getMinPower() {
        return minPower;
    }

    public void setMinPower(String minPower) {
        this.minPower = minPower;
    }

    public String getMaxPower() {
        return maxPower;
    }

    public void setMaxPower(String maxPower) {
        this.maxPower = maxPower;
    }

    public String getMinCovering() {
        return minCovering;
    }

    public void setMinCovering(String minCovering) {
        this.minCovering = minCovering;
    }

    public String getMaxCovering() {
        return maxCovering;
    }

    public void setMaxCovering(String maxCovering) {
        this.maxCovering = maxCovering;
    }

    public String getOffset() {
        return offset;
    }

    public void setOffset(String offset) {
        this.offset = offset;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getProducer() {
        return producer;
    }

    public void setProducer(String producer) {
        this.producer = producer;
    }

    public String getProtection() {
        return protection;
    }

    public void setProtection(String protection) {
        this.protection = protection;
    }
}
